function uefaEuro2016(teams, scores){
  let outcome = scores[0] === scores[1] ? 'teams played draw.' : 
                  scores[0] > scores[1] ? teams[0] + ' won!' : teams[1] + ' won!'
  return `At match ${teams[0]} - ${teams[1]}, ${outcome}`
}

